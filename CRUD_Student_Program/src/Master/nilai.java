    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        try {
            String hubung = "jdbc:mysql://localhost:3306/sekolah";
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection kon = DriverManager.getConnection(hubung,"root","");
            Statement stt = kon.createStatement();

            String checkSQL = "SELECT * FROM nilai WHERE kode_nilai = '" + kodenilai.getText() + "'";
            ResultSet rs = stt.executeQuery(checkSQL);

            if (rs.next()) {
                //A record with the same primary key already exists
                JOptionPane.showMessageDialog(this, "Kode nilai " + kodenilai.getText() + " sudah digunakan");
            } else {
                String SQL = "insert into nilai values('"+kodenilai.getText()+"','"+tnis.getSelectedItem()+"','"+tnip.getSelectedItem()+"','"+nilaiuh.getText()+"','"+nilaipraktek.getText()+"','"+nilaipts.getText()+"','"+nilaipas.getText()+"','"+totalnilai.getText()+"','"+rata_nilai.getText()+"')";
                stt.executeUpdate(SQL);
                data[0]=kodenilai.getText();
            }
            data[1]=tnis.getSelectedItem().toString();
            data[2]=tnip.getSelectedItem().toString();
            data[3]=nilaiuh.getText();
            data[4]=nilaipraktek.getText();
            data[5]=nilaipts.getText();
            data[6]=nilaipas.getText();
            data[7]=totalnilai.getText();
            data[8]=rata_nilai.getText();
            tblModel.insertRow(0, data);
            kodenilai.setText("");
            tnis.setSelectedItem("-=NIS=-");
            tnip.setSelectedItem("-=NIP=-");
            nilaiuh.setText("");
            nilaipraktek.setText("");
            nilaipts.setText("");
            nilaipas.setText("");
            totalnilai.setText("");
            rata_nilai.setText("");
            stt.close();
            kon.close();
        } catch(Exception exc) {
            System.err.println(exc.getMessage());
        }

    }//GEN-LAST:event_simpanActionPerformed

    
